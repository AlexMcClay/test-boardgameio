import type { Card, DamageEffect, EffectTypes } from "@/types";

const damage = (
  value: number | keyof Card,
  target: DamageEffect["target"] = "user-select",
  battlecry: boolean = false,
): EffectTypes => {
  return {
    type: "damage",
    value: value,
    target: target,
    battlecry: battlecry,
  };
};

const destroy = (
  target: "user-select" | "self" | "enemy",
  battlecry: boolean = false,
): EffectTypes => {
  return {
    type: "destroy",
    target: target,
    battlecry: battlecry,
  };
};

const mana = (value: number): EffectTypes => {
  return { type: "mana", value: value };
};
const heal = (
  value: number,
  target:
    | "user-select"
    | "friendly-hero"
    | "friendly-all"
    | "friendly-board" = "user-select",
  battlecry: boolean = false,
): EffectTypes => {
  return {
    type: "heal",
    value: value,
    target: target,
    battlecry: battlecry,
  };
};

const draw = (value: number): EffectTypes => {
  return {
    type: "draw",
    value: value,
  };
};

const changeKey = (
  key: keyof Card,
  value: any,
  target: "other" | "self" = "other",
): EffectTypes => {
  return {
    type: "changeKey",
    key: key,
    value: value,
    target: target,
  };
};

const incrementValue = (
  key: keyof Card,
  value: number,
  target: "other" | "self" = "other",
): EffectTypes => {
  return {
    type: "incrementValue",
    key: key,
    value: value,
    target: target,
  };
};

const summon = (cardID: string): EffectTypes => {
  return {
    type: "summon",
    cardID: cardID,
  };
};

